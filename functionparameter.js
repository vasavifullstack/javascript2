//function with parameters and arguments.

//addition with parameters.
function add(param1 , param2){
    let a=param1;
    let b=param2;
    let result=a+b;
    return result;
}
add();

//addition with arguments.
let a=add(50,30);
console.log(a);

let b=add(2000,5000);
console.log(b);

